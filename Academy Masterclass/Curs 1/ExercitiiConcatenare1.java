public class ExercitiiConcatenare1 {
    public static void main(String[] args) {
        int num1 = 10;
        int num2 = 20;
        System.out.println("Primul numar este " + num1 + "Iar al doilea numar este " + num2);


        int num3 = 25;
        int num4 = 30;
        int num5 = 29;
        int num6 = 23;
        System.out.println(num3 + num4 + num5 + num6);
        System.out.println(num3 / num4 / num5 / num6);
        System.out.println(num3 - num4 - num5 - num6);
        System.out.println(num3 & num4 & num5 & num6);
    }
}
