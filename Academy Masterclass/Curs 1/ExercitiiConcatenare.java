public class ExercitiiConcatenare {
    public static void main(String[] args) {

        int number = 10;
        String text = "Text";
        double myNumber = 12.2;
        float myNumber1 = 15.2f;
        boolean adv = true;
        char c = 'a';
        System.out.println("Valoarea numarului meu este "+number);
        System.out.println("Textul meu este "+text);
        System.out.println("Numarul meu este "+myNumber);
        System.out.println("Numarul meu este "+myNumber1);
        System.out.println("Raspunsul meu este " +adv);
        System.out.println("Characterul meu este "+c);
    }
}
