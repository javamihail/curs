public class Nested_For_Program {
    public static void main(String[] args) {
        /*
         * Write a program to create a pattern:
         *
         *       *
         *       **
         *       ***
         *       ****
         *       *****
         *       ******
         *
         */
        for (int j = 1; j <= 1; j++) {
            System.out.print("*");
        }
        System.out.println("\n");
        for (int j = 1; j <= 2; j++) {
            System.out.print("*");
        }
        System.out.println("\n");
        for (int j = 1; j <= 3; j++) {
            System.out.print("*");
        }
        System.out.println("\n");
        for (int j = 1; j <= 4; j++) {
            System.out.print("*");
        }
        System.out.println("\n");
        for (int j = 1; j <= 5; j++) {
            System.out.print("*");
        }
        System.out.println("\n");
        for (int j = 1; j <= 6; j++) {
            System.out.print("*");
        }
        System.out.println("\n");
    }
}
