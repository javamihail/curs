 /*
  * Logical and - &&
  * Logical OR - !!
  * Logical NOT - !
  * Bitwise AND - &
  * Bitwise OR - !
  * Bitwise OR - ~

  *
  */


 public class Logical {
     public static void main(String[] args) {


         int num1 = 60;  //111100  => 000011
         int num2 = 42;  // 101010
         // 101000 => 40
         // 11110 => 62

         System.out.println(num1 | num2);
         System.out.println(~num1);


     }
 }
