public class Operators {
    public static void main(String[] args) {
        int num1 = 10;
        int num2 = 20;
        int num3 = 30;
        int num4 = 40;
        num1++;
        num1 = num1+1;

        num2 --;
        num2 = num2 -1;

        num3+=5;
        num3 = num3 +5;

        num4 -=10;
        num4 = num4 -10;
        System.out.println(num4);
    }
}
